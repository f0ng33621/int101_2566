package work03;

public class MyUtil {

    public static double calculateBMI (double height,double weight){
        return (weight / (height * height));
    }
    public static double average (int v1, int v2, int v3){
        int total = (v1+v2+v3) / 3 ;
        double totalD = total;
        return totalD;
    }
}
